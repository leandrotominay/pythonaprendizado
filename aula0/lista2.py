# Para ordenar uma lista de numeros desordenados:
lista = [143, 35, 37, 94, 37, 102, 200, 1053, 2043, 950]
# Crescente:
crescente = sorted(lista)
print(crescente)
# Decrescente:
decrescente = sorted(lista, reverse=True)
print(decrescente)
# Ordenar alfabeticamente uma lista do A até o Z:
lista2 = ["bola", "abacate", "melancia"]
lista2.sort()
print(lista2)
# Ordenar alfabeticamente uma do Z até o A:
lista2 = ["bola", "abacate", "melancia"]
lista2.sort(reverse=True)
print(lista2)




